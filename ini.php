        <h4>Admisión 2018</h4>
        <p class="center-align responsive-text">ESTE AÑO DEBUTA EN LA REGIÓN DE LOS LAGOS EL NUEVO SISTEMA DE ADMISIÓN ESCOLAR DEL MINEDUC.</p>
        <p class="text-justify responsive-text">La Ley de Inclusión Escolar, promulgada el 2015, establece entre otras cosas, el fin de la selección en todos los establecimientos que reciban subvención del Estado, esto quiere decir que se termina con cualquier tipo de discriminación arbitraria en los procesos de admisión a los liceos municipales, colegios particular subvencionados y de administración delegada de todo el país, y que las niñas, niños y jóvenes que viven en Chile tendrán las mismas oportunidades de acceder a una educación de calidad, más inclusiva y equitativa.
            Bajo este marco, la Ley de Inclusión Escolar crea el Sistema de Admisión Escolar, para que las familias puedan postular a todos los establecimientos que deseen en un mismo lugar. Este nuevo sistema se irá implementando gradualmente en todo el país, para ello, se realizó como piloto, el año 2016 en la región de Magallanes y de la Antártica Chilena y siguiendo el año 2017 en la región de Tarapacá, Coquimbo, O’Higgins y Los Lagos.
            El Sistema cuenta con una plataforma web, que estará disponible a partir del 8 de agosto, para que las y los apoderados realicen sus postulaciones desde cualquier computador con acceso a internet. En este sitio se puede encontrar información detallada sobre todos los establecimienos municipales, particular subvencionados y de administración delegada de la región, tales como: Proyecto Educativo, Reglamento Interno, actividades extracurriculares, número de alumnos matriculados, tipo de jornada, modalidad y niveles de enseñanza, infraestructura, arancel mensual –en el caso de los colegios que no son gratuitos-, entre otros datos.</p>
